namespace Narumikazuchi.Windows.Wpf;

/// <summary>
/// The dock position of the window.
/// </summary>
public enum WindowDockPosition
{
    /// <summary>
    /// Not docked.
    /// </summary>
    Undocked,

    /// <summary>
    /// Window is docked to the left side of the display.
    /// </summary>
    Left,

    /// <summary>
    /// Window is docked to the right side of the display.
    /// </summary>
    Right
}